    private void tampilData() {
        MahasiswaNilai[] data = controller.getAll();
        DefaultTableModel model = (DefaultTableModel) tabel.getModel();
        model.setRowCount(0);
        for (MahasiswaNilai mhs : data){
            Object[] row = {
                mhs.getNIM(),
                mhs.getNama(),
                mhs.getTugas(),
                mhs.getUts(),
                mhs.getUas(),
                mhs.getNilaiAngka(),
                mhs.getNilaiHuruf()
            };
            model.addRow(row);
    }
    }
